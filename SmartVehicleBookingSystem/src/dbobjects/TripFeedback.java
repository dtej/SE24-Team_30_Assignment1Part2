package dbobjects;

public class TripFeedback {

	private int tripRating;
	private String tripFeedback;

	public int getTripRating() {
		return tripRating;
	}

	public void setTripRating(int tripRating) {
		this.tripRating = tripRating;
	}

	public String getTripFeedback() {
		return tripFeedback;
	}

	public void setTripFeedback(String tripFeedback) {
		this.tripFeedback = tripFeedback;
	}

}
